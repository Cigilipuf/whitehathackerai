    def _parse_verification_result_from_dict(self, data: dict | None) -> VerificationResult:
        """Parse dict into VerificationResult."""
        if not data:
            return VerificationResult()

        def _sf(v: Any, d: float = 0.0) -> float:
            if v is None:
                return d
            try:
                return float(v)
            except (ValueError, TypeError):
                return d

        cvss_val = data.get("cvss_override")
        cvss_override: float | None = None
        if cvss_val is not None:
            parsed = _sf(cvss_val, -1.0)
            if parsed >= 0.0:
                cvss_override = max(0.0, min(10.0, parsed))

        return VerificationResult(
            is_real=bool(data.get("is_real", False)),
            confidence=max(0.0, min(100.0, _sf(data.get("confidence", 0), 0.0))),  # Bug 5.2l-5: was 50/50.0
            reasoning=str(data.get("reasoning", "") or ""),
            additional_evidence=str(data.get("additional_evidence", "") or ""),
            suggested_poc_steps=data.get("suggested_poc_steps") or [],
            exploit_feasibility=str(data.get("exploit_feasibility", "unknown") or "unknown"),
            cvss_override=cvss_override,
        )

