    def _parse_attack_narratives(self, text: str) -> list[dict[str, str]]:
        """Parse free-form attack narratives from LLM response."""
        if not text:
            return []

        narratives: list[dict[str, str]] = []
        # Split on "SCENARIO:" or "---" delimiters
        import re as _re
        blocks = _re.split(r'(?:^|\n)---\s*\n|(?:^|\n)SCENARIO:', text)

        for block in blocks:
            block = block.strip()
            if not block or len(block) < 30:
                continue

            narrative_m = _re.search(r'NARRATIVE:\s*(.+?)(?=\nVULN_CLASS:|\nENDPOINT:|\nSEVERITY:|\Z)', block, _re.DOTALL)
