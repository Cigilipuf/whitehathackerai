    async def plan_attack_chain(
        self,
        findings: list[dict[str, Any]],
        technologies: list[str],
        context_summary: str,
    ) -> dict[str, Any]:
        """Plan a multi-step attack chain from existing findings.

        Returns a dict with ``chain_name`` (str), ``steps`` (list[dict]),
        ``expected_impact`` (str), ``confidence`` (float 0-1).
        """
        if not self.is_available:
            return {"chain_name": "", "steps": [], "expected_impact": "", "confidence": 0.0}

        from src.brain.prompts.agent_prompts import build_chain_attack_prompt

        prompt = build_chain_attack_prompt(
            findings=findings,
            technologies=technologies,
            context=context_summary,
        )

        data = await self._brain_call_json(
            prompt,
            system_prompt=(
                "You are planning an attack chain combining multiple low/medium "
                "findings into a high-impact exploit path. Think like a senior "
                "penetration tester. Reply in JSON with keys: chain_name (str), "
                "steps (list of {action, target, tool_or_technique}), "
                "expected_impact (str), confidence (float 0.0-1.0)."
            ),
            brain=BrainType.PRIMARY,
            timeout=120.0,
            task_type="chain_attack",
        )
        if data and isinstance(data, dict):
            return data
        return {"chain_name": "", "steps": [], "expected_impact": "", "confidence": 0.0}
