    def _parse_nuclei_template_from_dict(self, data: dict | None, tech: str) -> NucleiTemplate | None:
        """Parse dict into NucleiTemplate, with YAML structure validation."""
        if not data or not data.get("yaml_content"):
            return None

        yaml_content = data["yaml_content"]

        # ── Validate nuclei YAML structure ──
        try:
            import yaml as _yaml
            parsed = _yaml.safe_load(yaml_content)
            if not isinstance(parsed, dict):
                logger.warning("Nuclei template: YAML did not parse to dict")
                return None

            # Must have 'id' and 'info' at root
            if "id" not in parsed or "info" not in parsed:
                logger.warning("Nuclei template: missing 'id' or 'info' at root level")
                return None

            # Must have 'http' (or 'network', 'dns', 'headless', 'code', 'flow')
            valid_protocol_keys = {"http", "network", "dns", "headless", "code", "flow", "tcp", "ssl"}
            if not any(k in parsed for k in valid_protocol_keys):
                # Try to auto-fix: if "requests" or "template" key exists, restructure
                if "template" in parsed and isinstance(parsed["template"], dict):
                    inner = parsed["template"]
                    if "requests" in inner:
                        parsed["http"] = inner["requests"]
                        del parsed["template"]
                        logger.info("Nuclei template: auto-fixed 'template.requests' → 'http'")
                elif "requests" in parsed:
                    parsed["http"] = parsed.pop("requests")
                    logger.info("Nuclei template: auto-fixed 'requests' → 'http'")
                else:
                    logger.warning("Nuclei template: no protocol key (http/network/etc.) found")
                    return None

                # Remove non-standard root keys
                for bad_key in ("matchers", "evidence", "template"):
                    parsed.pop(bad_key, None)

                # Re-serialize the fixed YAML
                yaml_content = _yaml.dump(parsed, default_flow_style=False, sort_keys=False)

        except Exception as e:
            logger.warning(f"Nuclei template YAML validation failed: {e}")
            return None

        return NucleiTemplate(
            template_id=data.get("template_id", f"custom-{tech.lower().replace(' ', '-')}"),
            name=data.get("name", ""),
            severity=data.get("severity", "medium"),
            description=data.get("description", ""),
            yaml_content=yaml_content,
            target_tech=tech,
        )

