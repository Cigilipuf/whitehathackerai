    @staticmethod
    def _cluster_urls(urls: list[str], max_clusters: int = 30) -> dict[str, list[str]]:
        """Group URLs by path pattern, replacing dynamic segments with {id}.

        Returns mapping from pattern → representative URLs (max 3 each).
        E.g. "/api/v1/users/123" and "/api/v1/users/456" → "/api/v1/users/{id}" → [both]
        """
        from urllib.parse import urlparse
        import re as _re

        clusters: dict[str, list[str]] = {}
        _DYNAMIC_SEG = _re.compile(
            r'/(?:'
            r'[0-9a-f]{8,}'         # hex ids / uuids
            r'|[0-9]+'             # numeric ids
            r'|[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}'  # full uuid
            r')(?=/|$)'
        )

        for u in urls:
            try:
                parsed = urlparse(u)
                pattern = _DYNAMIC_SEG.sub('/{id}', parsed.path)
                # Normalize trailing slash
                pattern = pattern.rstrip('/') or '/'
                if pattern not in clusters:
                    if len(clusters) >= max_clusters:
                        continue
                    clusters[pattern] = []
                if len(clusters[pattern]) < 3:
                    clusters[pattern].append(u)
            except Exception:
                continue
        return clusters

    @staticmethod
    def _compact_tech_stack(technologies: dict[str, list[str]]) -> dict[str, list[str]]:
        """Merge per-host tech lists into a single deduplicated summary.

        Returns {"frameworks": [...], "servers": [...], "languages": [...], "other": [...]}.
        """
        all_techs: set[str] = set()
        for techs in technologies.values():
            all_techs.update(techs)

        # Categorize by keyword heuristics
        frameworks: list[str] = []
        servers: list[str] = []
        languages: list[str] = []
        other: list[str] = []

        _fw_kw = {"react", "angular", "vue", "django", "flask", "laravel", "rails",
                   "spring boot", "express", "next.js", "nuxt", "wordpress", "drupal", "joomla",
                   "symfony", "fastapi", "jquery", "bootstrap"}
        _srv_kw = {"nginx", "apache", "iis", "tomcat", "caddy", "lighttpd", "envoy",
                    "haproxy", "cloudflare", "varnish", "litespeed", "openresty",
                    "gunicorn", "uwsgi", "puma", "unicorn"}
        _lang_kw = {"php", "python", "java", "ruby", "node.js", "golang", "rust", "perl",
                     "asp.net", "c#", ".net", "typescript", "javascript"}

        for t in sorted(all_techs):
            t_lower = t.lower()
            if any(kw in t_lower for kw in _fw_kw):
                frameworks.append(t)
            elif any(kw in t_lower for kw in _srv_kw):
                servers.append(t)
            elif any(kw in t_lower for kw in _lang_kw):
                languages.append(t)
            else:
                other.append(t)

        result: dict[str, list[str]] = {}
        if frameworks:
            result["frameworks"] = frameworks[:10]
        if servers:
            result["servers"] = servers[:5]
        if languages:
            result["languages"] = languages[:5]
        if other:
            result["other"] = other[:10]
        return result

    @staticmethod
    def _compact_ports(open_ports: dict[str, list[int]]) -> dict[str, list[int]]:
        """Deduplicate ports across hosts — keep unique port→host mapping."""
        all_ports: set[int] = set()
        for ports in open_ports.values():
            all_ports.update(ports)
        # Interesting non-standard ports first
        standard = {80, 443, 22, 21, 25, 53, 110, 143}
        interesting = sorted(p for p in all_ports if p not in standard)[:20]
        std_present = sorted(p for p in all_ports if p in standard)
        return {
            "standard": std_present,
            "non_standard": interesting,
            "total_hosts": len(open_ports),
        }

