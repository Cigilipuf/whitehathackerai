# ── Prompt Credential Sanitization ────────────────────────────────
# Matches key=value or key: value patterns for sensitive fields.
# Applied before any content is sent to the LLM brain to prevent
# accidental credential leakage through recon data, OSINT results,
# or tool output that may contain discovered secrets.
_PROMPT_SENSITIVE_PATTERNS: tuple[str, ...] = (
    "api_key", "api_token", "apikey", "secret_key", "client_secret",
    "password", "credential", "bearer", "cookie",
    "jwt", "private_key", "access_key",
    "refresh_token", "authorization", "auth_token", "auth_key",
    "aws_secret", "aws_access", "slack_token", "github_token",
    "database_url", "db_password", "mysql_pwd", "postgres_password",
)
_PROMPT_SENSITIVE_RE = re.compile(
    r'\b(' + '|'.join(re.escape(p) for p in _PROMPT_SENSITIVE_PATTERNS) + r')\s*[=:]\s*\S+',
    re.IGNORECASE,
)


def _sanitize_prompt(text: str) -> str:
    """Redact credential-like patterns from text before sending to LLM."""
    if not text:
        return text
    return _PROMPT_SENSITIVE_RE.sub(r'\1=***REDACTED***', text)


