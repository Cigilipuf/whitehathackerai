    async def _brain_call_json(
        self,
        prompt: str,
        system_prompt: str = "",
        brain: BrainType = BrainType.PRIMARY,
        timeout: float = 1200.0,
        task_type: str = "",
    ) -> dict | list | None:
        """Brain call that guarantees parsed JSON — retries once on parse failure.

        Returns parsed dict/list on success, None on failure. This replaces the
        pattern of `_brain_call(json_mode=True)` + `_safe_json_parse()` with
        a single call that automatically retries with a correction prompt if
        the first response isn't valid JSON.
        """
        response = await self._brain_call(
            prompt=prompt,
            system_prompt=system_prompt,
            brain=brain,
            json_mode=True,
            timeout=timeout,
            task_type=task_type,
        )

        if not response:
            self._json_parse_fail += 1
            return None

        # Try parsing the response
        data = self._safe_json_parse(response)
        if data is not None:
            self._json_parse_ok += 1
            return data

        # Parse failed — retry once with explicit correction prompt
        logger.warning(
            f"Intelligence JSON parse failed (#{self._call_count}), "
            f"retrying with correction prompt | response_preview={response[:200]!r}"
        )

        correction_prompt = (
            "Your previous response was NOT valid JSON and could not be parsed.\n"
            f"Here is what you returned (first 500 chars):\n{response[:500]}\n\n"
            "Please return ONLY the corrected, valid JSON. "
            "No markdown fences, no text outside the JSON. "
            "If the data is a list, start with [ and end with ]. "
            "If the data is an object, start with {{ and end with }}."
        )

        retry_response = await self._brain_call(
            prompt=correction_prompt,
            system_prompt=system_prompt,
            brain=brain,
            json_mode=True,
            timeout=min(timeout, 120.0),
            task_type=task_type,
        )

        if retry_response:
            data = self._safe_json_parse(retry_response)
            if data is not None:
                self._json_parse_retry_ok += 1
                logger.info(f"Intelligence JSON retry succeeded (#{self._call_count})")
                return data

        self._json_parse_fail += 1
        logger.warning(f"Intelligence JSON retry also failed (#{self._call_count})")
        return None

    async def _brain_call(
        self,
        prompt: str,
        system_prompt: str = "",
        brain: BrainType = BrainType.PRIMARY,
        json_mode: bool = False,
        timeout: float = 1200.0,
        task_type: str = "",
    ) -> str:
        """Wrapped brain.think() call with logging, timing, caching and error handling."""
        # Route via BrainRouter if available and task_type is specified
        if self.router and task_type:
            brain = self.router.route(task_type)

        # Guard: skip immediately if brain is confirmed down
        if not self.is_available:
            return ""

        self._call_count += 1
        t0 = time.time()

        # E1: Check cache for identical prompts
        cache_key = hashlib.sha256(
            f"{brain.value}:{system_prompt}:{prompt}:{json_mode}".encode()
        ).hexdigest()[:16]

        async with self._cache_lock:
            if cache_key in self._cache:
                cached_text, cached_time = self._cache[cache_key]
                if (time.time() - cached_time) < self._cache_ttl:
                    self._cache_hits += 1
                    self._cache.move_to_end(cache_key)  # LRU: most recent at end
                    logger.info(
                        f"Intelligence call #{self._call_count} CACHE HIT | "
                        f"brain={brain.value} | hits={self._cache_hits}"
                    )
                    return cached_text
                else:
                    # Expired entry
                    del self._cache[cache_key]

        try:
            # Sanitize credentials from prompt content before sending to LLM
            sanitized_prompt = _sanitize_prompt(prompt)
            sanitized_system = _sanitize_prompt(system_prompt)

            response: BrainResponse = await asyncio.wait_for(
                self.brain.think(
                    prompt=sanitized_prompt,
                    brain=brain,
                    system_prompt=sanitized_system,
                    json_mode=json_mode,
                ),
                timeout=timeout,
            )
            elapsed = time.time() - t0
            self._total_time += elapsed

            logger.info(
                f"Intelligence call #{self._call_count} | "
                f"brain={brain.value} | time={elapsed:.1f}s | "
                f"tokens={response.tokens_used} | "
                f"text_len={len(response.text)}"
            )

            self._brain_call_success += 1

            # Cache successful non-empty responses
            if response.text.strip():
                async with self._cache_lock:
                    if len(self._cache) >= self._cache_max_size:
                        # Evict least-recently-used entry (first in order)
                        self._cache.popitem(last=False)
                    self._cache[cache_key] = (response.text, time.time())
                # Reset failure counter on success
                self._consecutive_failures = 0

            # D3: Log brain CoT thinking content if available
            if response.metadata.get("thinking"):
                logger.debug(
                    f"Brain thinking (#{self._call_count}): "
                    f"{response.metadata['thinking'][:300]}..."
                )

            # Track empty responses
            if not response.text.strip():
                self._brain_call_empty += 1

            return response.text

        except asyncio.TimeoutError:
            elapsed = time.time() - t0
            # H4 fix: retry once with 60% timeout before giving up
            retry_timeout = timeout * 0.6
            if not self._brain_down and retry_timeout >= 15:
                logger.info(
                    f"Intelligence call #{self._call_count} timed out after {elapsed:.1f}s, "
                    f"retrying with {retry_timeout:.0f}s timeout"
                )
                try:
                    response = await asyncio.wait_for(
                        self.brain.think(
                            prompt=sanitized_prompt, brain=brain,
                            system_prompt=sanitized_system, json_mode=json_mode,
                        ),
                        timeout=retry_timeout,
                    )
                    if response.text.strip():
                        self._consecutive_failures = 0
                        async with self._cache_lock:
                            if len(self._cache) >= self._cache_max_size:
                                self._cache.popitem(last=False)
                            self._cache[cache_key] = (response.text, time.time())
                        logger.info(f"Intelligence call #{self._call_count} retry succeeded")
                        return response.text
                except (asyncio.TimeoutError, Exception) as _retry_err:
                    logger.debug("Intelligence retry also failed: %s", _retry_err)

            self._consecutive_failures += 1
            self._check_brain_down()
            self._brain_call_error += 1
            return ""
        except (RuntimeError, ConnectionError, OSError) as e:
            elapsed = time.time() - t0
            self._consecutive_failures += 1
            logger.warning(
                f"Intelligence call #{self._call_count} failed | "
                f"brain={brain.value} | time={elapsed:.1f}s | error={type(e).__name__}: {e} | "
                f"consecutive_failures={self._consecutive_failures}"
            )
            self._check_brain_down()
            self._brain_call_error += 1
            # Re-raise critical errors that indicate no model is available
            if isinstance(e, RuntimeError) and "No brain model available" in str(e):
                self._brain_down = True
                logger.error("Brain confirmed DOWN — No brain model available. All future brain calls will be skipped.")
                raise
            return ""
        except Exception as e:
            elapsed = time.time() - t0
            self._consecutive_failures += 1
            logger.error(
                f"Intelligence call #{self._call_count} unexpected error | "
                f"brain={brain.value} | time={elapsed:.1f}s | error={type(e).__name__}: {e} | "
                f"consecutive_failures={self._consecutive_failures}"
            )
            self._check_brain_down()
            self._brain_call_error += 1
            return ""

    def _check_brain_down(self) -> None:
        """B2: Mark brain as down after N consecutive failures."""
        if self._consecutive_failures >= self._brain_down_threshold and not self._brain_down:
            self._brain_down = True
            self._brain_down_time = time.time()
            logger.error(
                f"Brain marked as DOWN after {self._consecutive_failures} consecutive failures. "
                f"Will auto-retry after {self._brain_down_recovery_secs:.0f}s. "
                f"All intelligence calls will be skipped until then."
            )

    def _safe_json_parse(self, text: str) -> dict | list | None:
        """Parse JSON from LLM response, handling markdown code blocks.
        
        May return a dict OR a list depending on the LLM response.
        """
        if not text:
            return None

        # Try direct parse first
        try:
            return json.loads(text)
        except json.JSONDecodeError:
            pass

        # Extract from markdown code block
        patterns = [
            r'```json\s*\n(.*?)\n```',
            r'```\s*\n(.*?)\n```',
        ]

        for pattern in patterns:
            match = re.search(pattern, text, re.DOTALL)
            if match:
                try:
                    candidate = match.group(1)
                    return json.loads(candidate)
                except (json.JSONDecodeError, IndexError):
                    continue

        # Find valid { ... } or [ ... ] using shared robust JSON extractor
        from src.utils.json_utils import extract_json
        # Try object first, then array
        result = extract_json(text, log_failures=False)
        if result is not None:
            return result
        return extract_json(text, expect_array=True, log_failures=True)

