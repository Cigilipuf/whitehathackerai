    @property
    def is_available(self) -> bool:
        if self._brain_down:
            # Auto-recover after _brain_down_recovery_secs
            if self._brain_down_time > 0 and (time.time() - self._brain_down_time) >= self._brain_down_recovery_secs:
                logger.info(
                    f"Brain auto-recovery: {self._brain_down_recovery_secs:.0f}s elapsed since brain-down. "
                    f"Resetting brain-down flag to allow retry."
                )
                self._brain_down = False
                self._consecutive_failures = 0
                self._brain_down_time = 0.0
                # Fall through to normal availability check below
            else:
                return False
        return self.brain is not None and (
            self.brain.has_primary or self.brain.has_secondary
        )

